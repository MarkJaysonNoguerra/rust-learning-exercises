fn main() {
    // addition
    let sum = 5 + 100;
    println!("{sum}");

    // subtraction
    let difference = 95.5 - 4.3;
    println!("{difference}");

    // multiplcation
    let product = 4 * 30;
    println!("{product}");

    // division
    let quotient = 57.7 / 43.2;
    println!("{quotient}");
    let truncated = -5 / 3;
    println!("{truncated}");

    // remidner
    let remainder = 43 % 5;
    println!("{remainder}");
}
